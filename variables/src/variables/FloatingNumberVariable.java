package variables;

public class FloatingNumberVariable {
	
	public static void main(String[] args) {
		
		// 실수 - float. double
		// float type: 소수점 이하 7자리까지 출력
		float floatNumber = 3.0f;
		float floatNumber1 = 3;
		float floatNumber2 = 3.4444f;
		double doubleNumber = 3.12345678901234567890;
		
		System.out.println(floatNumber);  // 3.0
		System.out.println(floatNumber1); // 3.0
		System.out.println(floatNumber2); // 3.4444
		System.out.println(doubleNumber); // 3.1234567890123457
		

	}

}
